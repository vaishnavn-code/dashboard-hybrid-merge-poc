// take first 10 records
const BASE_DATA = RAW_DATA_PAYLOAD.slice(0, 10);

const generateMonthlyData = () => {
  const result = [];
  const year = "2025";

  for (let m = 1; m <= 12; m++) {
    const month = String(m).padStart(2, "0");

    for (let i = 0; i < 10; i++) {
      const baseRecord = BASE_DATA[i % BASE_DATA.length];

      const newRecord = { ...baseRecord };

      // update month/year
      newRecord.Month = month;
      newRecord.Year = year;

      // unique txn number
      newRecord["Txn No"] = (
        parseInt(baseRecord["Txn No"].trim()) +
        m * 100 +
        i
      )
        .toString()
        .padEnd(12, " ");

      // update dates
      newRecord["Start Date"] = `${year}${month}01`;
      newRecord["End Date"] = `${year}${month}28`;

      result.push(newRecord);
    }
  }

  return result;
};

// 🔹 Generate data
const generatedData = generateMonthlyData();

// 🔹 Write to file
